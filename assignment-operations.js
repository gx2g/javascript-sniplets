/*
Operations with Assignment

Addition Assignment (+=);
Substraction Assignment (-=);
Multiplication Assignment (*=);
Division Assignment (/=);
Remainder (Modulo) Assignment (%=);
Exponentiation Assignment (**=);

*/

let a = 17;

a += 5; // same as a = a + 5;
console.log(a); // answer: 22

a -= 6; // a = a - 6
console.log(a); // answer: 16

a *= 6; // a = a * 6
console.log(a); // answer: 96

a /= 6; // a = a / 6
console.log(a); // answer: 16

a %= 6; // a = a % 6
console.log(a); // answer: 4

a **= 6; // a = a ** 6
console.log(a); // answer: 4096
