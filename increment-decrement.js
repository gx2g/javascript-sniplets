/*
Increment and Decrement are operations 
used to increase or decrease a 
variable's value by 1.


*/

let a = 0;

a++;

console.log(a); // answer: 1

a++;

console.log(a); // answer: 2

a++; 

console.log(a); // answer: 3

/* 

The expression a++ is
equivalent to a = a + 1 or a += 1.

*/

let a = 5;

a--;
console.log(a); // answer: 4

a--;
console.log(a); // answer: 3

a--;
console.log(a); // answer: 2

/*

The expression a-- is 
equivalent to a = a - 1 or a -= 1.

*/